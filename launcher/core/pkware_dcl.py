def _build_lookup(codes, bits, count):
    """Build fast lookup table: for each possible bit pattern → (symbol, nbits).

    The codes in the PKWare DCL tables are already in LSB-first order
    as they appear in the bit buffer. No bit reversal needed.
    """
    max_bits = max(bits[:count])
    table_size = 1 << max_bits
    lookup = [None] * table_size

    for sym in range(count):
        nbits = bits[sym]
        if nbits == 0:
            continue
        code = codes[sym]

        # Fill all table entries where the low `nbits` bits match `code`
        step = 1 << nbits
        idx = code
        while idx < table_size:
            lookup[idx] = (sym, nbits)
            idx += step

    return lookup, max_bits


class _BitStream:
    __slots__ = ('data', 'pos', 'buf', 'bits')

    def __init__(self, data: bytes):
        self.data = data
        self.pos = 0
        self.buf = 0
        self.bits = 0

    def _ensure(self, n):
        while self.bits < n:
            if self.pos >= len(self.data):
                return
            self.buf |= self.data[self.pos] << self.bits
            self.pos += 1
            self.bits += 8

    def read(self, n):
        self._ensure(n)
        val = self.buf & ((1 << n) - 1)
        self.buf >>= n
        self.bits -= n
        return val

    def peek(self, n):
        self._ensure(n)
        return self.buf & ((1 << n) - 1)

    def skip(self, n):
        self.buf >>= n
        self.bits -= n


def explode(data: bytes) -> bytes:
    """Decompress PKWare DCL compressed data.

    Args:
        data: Compressed data (without the MPQ compression type byte)

    Returns:
        Decompressed data
    """
    bs = _BitStream(data)

    # Header
    ctype = bs.read(8)   # 0 = binary, 1 = ASCII
    dsize = bs.read(8)   # dictionary size bits: 4, 5, or 6

    if ctype not in (0, 1):
        raise ValueError(f"Invalid compression type: {ctype}")
    if dsize not in (4, 5, 6):
        raise ValueError(f"Invalid dict size bits: {dsize}")

    # Build decode tables
    len_lookup, len_max = _build_lookup(_LEN_CODE, _LEN_BITS, 16)
    dist_lookup, dist_max = _build_lookup(_DIST_CODE, _DIST_BITS, 64)

    if ctype == 1:
        asc_lookup, asc_max = _build_lookup(_CH_CODE_ASC, _CH_BITS_ASC, 256)

    out = bytearray()

    while True:
        if bs.read(1) == 0:
            # ── Literal byte ──
            if ctype == 1:
                # ASCII mode - decode via Huffman
                peek_val = bs.peek(asc_max)
                entry = asc_lookup[peek_val]
                if entry is None:
                    raise ValueError("Invalid ASCII code")
                sym, nbits = entry
                bs.skip(nbits)
                out.append(sym)
            else:
                # Binary mode - raw 8 bits
                out.append(bs.read(8))
        else:
            # ── Length-distance copy ──

            # Decode length index
            peek_val = bs.peek(len_max)
            entry = len_lookup[peek_val]
            if entry is None:
                raise ValueError("Invalid length code")
            len_idx, nbits = entry
            bs.skip(nbits)

            # Get actual length
            extra = _EX_LEN_BITS[len_idx]
            if extra:
                length = _LEN_BASE[len_idx] + bs.read(extra)
            else:
                length = _LEN_BASE[len_idx]

            # Check for end marker (length index 15 with extra bits = 0xFF -> total 0x108 + 0xFF = 0x207 = 519)
            # Actually, end is when len_idx == 15 and the extra value makes length == 0x108 + 0xFF = 519
            # But the real end marker is length == 0x10E (270) ... no.
            # End marker: length value 519 (0x207)
            if length == 0x0108 + 0xFF:  # 519
                break

            # Decode distance index
            peek_val = bs.peek(dist_max)
            entry = dist_lookup[peek_val]
            if entry is None:
                raise ValueError("Invalid distance code")
            dist_idx, nbits = entry
            bs.skip(nbits)

            # Calculate actual distance
            if length == 2:
                # For length 2, distance uses 2 extra bits
                dist = (dist_idx << 2) | bs.read(2)
            else:
                # For length > 2, distance uses dict_size extra bits
                dist = (dist_idx << dsize) | bs.read(dsize)

            dist += 1  # 1-based

            # Copy from output buffer
            src = len(out) - dist
            if src < 0:
                raise ValueError(f"Distance {dist} exceeds output size {len(out)}")

            for i in range(length):
                out.append(out[src + i])

    return bytes(out)
